#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	int			i;
	char		check;

	check = (char) c;
	i = (int) ft_strlen(s);
	while (i >= 0)
	{
		if (s[i] == check)
			return ((char *)&s[i]);
		i--;
	}
	return (NULL);
}
